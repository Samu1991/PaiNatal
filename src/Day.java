public class Day {

    private int n_familias;
    private int n_pessoas;
    private double cost;

    public Day() {
        this.n_familias = 0;
        this.n_pessoas = 0;
    }

    public int getN_familias() {
        return n_familias;
    }

    public void setN_familias(int n_familias) {
        this.n_familias = n_familias;
    }

    public int getN_pessoas() {
        return n_pessoas;
    }

    public void setN_pessoas(int n_pessoas) {
        this.n_pessoas = n_pessoas;
    }

    public double getCost() {
        return cost;
    }

    public void setCost(double cost) {
        this.cost = cost;
    }


}
